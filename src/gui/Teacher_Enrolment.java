    private void loadTeachers() {

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        try {

            ResultSet rs = MySQL.execute("SELECT * FROM `teachers` INNER JOIN `gender` ON `teachers`.`gender_id` = `gender`.`id` "
                    + "INNER JOIN `teacher_address` ON `teachers`.`Tno` = `teacher_address`.`teachers_Tno` INNER JOIN `subjects` ON `teachers`.`subjects_Subno` = `subjects`.`Subno` ORDER BY `reg_datetime` ASC");

            while (rs.next()) {

                Vector<String> vec = new Vector<>();
                vec.add(rs.getString("Tno"));
                vec.add(rs.getString("teFname") + " " + rs.getString("teLname"));
                vec.add(rs.getString("te_NIC"));
                vec.add(rs.getString("gender.gen"));
                vec.add(rs.getString("teMobile"));
                vec.add(rs.getString("teEmail"));
                vec.add(rs.getString("subjects.name"));

                model.addRow(vec);
                jTable1.setModel(model);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }


