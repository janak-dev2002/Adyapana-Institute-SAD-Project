    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:

        if (evt.getClickCount() == 2) {

            genarate_btn.setEnabled(false);
            save_btn.setText("UPDATE");
            clr_btn.setText("DELETE");

            int slectedRow = jTable1.getSelectedRow();
            String Selected_TeuNo = String.valueOf(jTable1.getValueAt(slectedRow, 0));

            try {
                ResultSet rs = MySQL.execute("SELECT * FROM `teachers` INNER JOIN `gender` ON `teachers`.`gender_id` = `gender`.`id` "
                        + "INNER JOIN `teacher_address` ON `teachers`.`Tno` = `teacher_address`.`teachers_Tno` INNER JOIN `subjects` ON `teachers`.`subjects_Subno` = `subjects`.`Subno` WHERE `Tno` = '" + Selected_TeuNo + "'");

                if (rs.next()) {

                    te_no.setText(rs.getString("Tno"));
                    te_fname.setText(rs.getString("teFname"));
                    te_lname.setText(rs.getString("teLname"));
                    te_nic.setText(rs.getString("te_NIC"));

                    Date dobDate = new Date();
                    SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
                    dobDate = dateFormat.parse(rs.getString("teDob"));
                    jDateChooser1.setDate(dobDate);

                    if ("Male".equals(rs.getString("gender.gen"))) {
                        jRadioButton1.setSelected(true);
                    } else {
                        jRadioButton2.setSelected(true);
                    }

                    te_mobile.setText(rs.getString("teMobile"));
                    te_email.setText(rs.getString("teEmail"));
                    te_line1.setText(rs.getString("teacher_address.line1"));
                    te_line2.setText(rs.getString("teacher_address.line2"));
                    cityCom.setSelectedIndex(rs.getInt("teacher_address.city_id"));
                    edu_quali.setText(rs.getString("teEduQualifi"));
                    prof_quali.setText(rs.getString("teProfQualifi"));
                    subCom.setSelectedIndex(rs.getInt("subjects_Subno"));

                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jTable1MouseClicked

