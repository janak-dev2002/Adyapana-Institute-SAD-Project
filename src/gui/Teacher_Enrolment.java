    private void loadCountry() {

        try {
            ResultSet rs = MySQL.execute("SELECT * FROM `city`");

            Vector cityVector = new Vector();
            cityVector.add("Select City");

            while (rs.next()) {
                cityVector.add(rs.getString("name"));
                cityMap.put(rs.getString("name"), rs.getInt("id"));
            }

            DefaultComboBoxModel model = new DefaultComboBoxModel(cityVector);
            cityCom.setModel(model);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

