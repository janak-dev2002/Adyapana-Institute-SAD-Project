    private void st_addingBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_st_addingBtnActionPerformed
        // TODO add your handling code here:

        if (stNo_txt.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please Enter the student number", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (st_sub_com.getSelectedItem().equals("Select Subject")) {
            JOptionPane.showMessageDialog(this, "Please select a subject", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (st_tea_COm.getSelectedItem().equals("Select Teacher")) {
            JOptionPane.showMessageDialog(this, "Please select a teacher", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (st_class_com.getSelectedItem().equals("Select Class")) {
            JOptionPane.showMessageDialog(this, "Please select a class", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            String Stu_No = stNo_txt.getText();
            int Class_no = classMap.get(st_class_com.getSelectedItem());

            // System.out.println(Class_no);
            Date date = new Date();
            SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
            String currentDT = format.format(date);

            try {

                MySQL.execute("INSERT INTO `student_enrolment_class` (`enrol_date`,`students_Sno`,`class_classNo`)"
                        + "VALUES ('" + currentDT + "','" + Stu_No + "','" + Class_no + "')");

                JOptionPane.showMessageDialog(this, "Student Assign Successfull", "System Message", JOptionPane.INFORMATION_MESSAGE);

                stu_nameLbl.setText(studentsMap.get(Stu_No).getFirstName() + " " + studentsMap.get(Stu_No).getLastName());
                stu_numLbl.setText(studentsMap.get(Stu_No).getStudentNumber());
                stu_nicLbl.setText(studentsMap.get(Stu_No).getNic());
                stu_mobLbl.setText(studentsMap.get(Stu_No).getMobile());

                clearAsign();
                loadStudentEnrolClass("");

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_st_addingBtnActionPerformed


    private void loadTeacherInfor(String Te_no) {

        try {
            ResultSet rs = MySQL.execute("SELECT * FROM `teachers` INNER JOIN `subjects` ON `teachers`.`subjects_Subno` = `subjects`.`Subno` WHERE `Tno` = '" + Te_no + "'");

            if (rs.next()) {
                teacher_nameLbl.setText(rs.getString("teFname") + " " + rs.getString("teLname"));
                teacher_mobLbl.setText(rs.getString("teMobile"));
                teacher_subjectLbl.setText(rs.getString("subjects.name"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

