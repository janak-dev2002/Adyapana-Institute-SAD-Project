    private void loadStudentEnrolClass(String class_nam) {

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
   
        String query;
        
        if(class_nam.equals("")){
             query = "SELECT * FROM `student_enrolment_class` INNER JOIN `class` ON `student_enrolment_class`.`class_classNo` = `class`.`classNo`"
                    + "INNER JOIN `students` ON `student_enrolment_class`.`students_Sno` = `students`.`Sno`";
        }else{
             query = "SELECT * FROM `student_enrolment_class` INNER JOIN `class` ON `student_enrolment_class`.`class_classNo` = `class`.`classNo`"
                    + "INNER JOIN `students` ON `student_enrolment_class`.`students_Sno` = `students`.`Sno` WHERE `classNo` = '"+classMap.get(class_nam)+"'";
        }
            
        try {

            ResultSet rs = MySQL.execute(query);

            while (rs.next()) {

                Vector<String> vec = new Vector<>();
                vec.add(rs.getString("class.class_name"));

                ResultSet te_rs = MySQL.execute("SELECT * FROM `teachers` WHERE `Tno` = '" + rs.getString("teachers_Tno") + "'");
                if (te_rs.next()) {
                    vec.add(te_rs.getString("teFname") + " " + te_rs.getString("teLname"));
                }

                vec.add(rs.getString("students.Sno"));
                vec.add(rs.getString("students.stFname") + " " + rs.getString("students.stLname"));
                vec.add(rs.getString("enrol_date"));

                ResultSet sub_rs = MySQL.execute("SELECT * FROM `subjects` WHERE `Subno` = '" + rs.getString("subjects_Subno") + "'");
                if (sub_rs.next()) {
                    vec.add(sub_rs.getString("name"));
                }

                model.addRow(vec);
                jTable1.setModel(model);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }


