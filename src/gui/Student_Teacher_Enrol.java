    private void loadCurrentClass(String stu_no) {

        DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
        model.setRowCount(0);

        try {

            ResultSet rs = MySQL.execute("SELECT * FROM `student_enrolment_class` INNER JOIN `class` ON `student_enrolment_class`.`class_classNo` = `class`.`classNo`"
                    + "WHERE `students_Sno` = '" + stu_no + "'");

            while (rs.next()) {

                Vector<String> vec = new Vector<>();
                vec.add(rs.getString("class.class_name"));
                vec.add(subMap.get(rs.getInt("class.subjects_Subno")));

                model.addRow(vec);
                jTable3.setModel(model);

            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

