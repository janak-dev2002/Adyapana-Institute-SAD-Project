    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:

        if (evt.getClickCount() == 2) {

            st_no_genarate.setEnabled(false);;
            save_btn.setText("UPDATE");
            clr_btn.setText("DELETE");

            int slectedRow = jTable2.getSelectedRow();
            String Selected_StuNo = String.valueOf(jTable2.getValueAt(slectedRow, 0));

            try {
                ResultSet rs = MySQL.execute("SELECT * FROM `students` INNER JOIN `gender` ON `students`.`gender_id` = `gender`.`id` "
                        + "INNER JOIN `student_address` ON `students`.`Sno` = `student_address`.`students_Sno` WHERE `Sno` = '" + Selected_StuNo + "'");

                if (rs.next()) {

                    st_no.setText(rs.getString("Sno"));
                    st_fname.setText(rs.getString("stFname"));
                    st_lname.setText(rs.getString("stLname"));
                    st_nic.setText(rs.getString("stNic"));

                    Date dobDate = new Date();
                    SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
                    dobDate = dateFormat.parse(rs.getString("dob"));
                    jDateChooser1.setDate(dobDate);

                    if ("Male".equals(rs.getString("gender.gen"))) {
                        jRadioButton1.setSelected(true);
                    } else {
                        jRadioButton2.setSelected(true);
                    }

                    mobile.setText(rs.getString("stMobile"));
                    email.setText(rs.getString("stEmail"));
                    address1.setText(rs.getString("student_address.line1"));
                    address2.setText(rs.getString("student_address.line2"));
                    city_Combo.setSelectedIndex(rs.getInt("student_address.city_id"));
                    g_name.setText(rs.getString("stGuardName"));
                    g_occu.setText(rs.getString("stGuardOccu"));
                    g_mobile.setText(rs.getString("stGuardMobile"));
                    g_email.setText(rs.getString("stGuradEmail"));

                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jTable2MouseClicked

