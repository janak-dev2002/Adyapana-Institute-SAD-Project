    private void loadCity() {

        try {
            ResultSet rs = MySQL.execute("SELECT * FROM `city`");

            Vector cityVector = new Vector();
            cityVector.add("Select City");

            while (rs.next()) {
                cityVector.add(rs.getString("name"));
                cityMap.put(rs.getString("name"), rs.getInt("id"));
            }

            DefaultComboBoxModel model = new DefaultComboBoxModel(cityVector);
            city_Combo.setModel(model);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void loadStudents() {

        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0);

        try {

            ResultSet rs = MySQL.execute("SELECT * FROM `students` INNER JOIN `gender` ON `students`.`gender_id` = `gender`.`id` "
                    + "INNER JOIN `student_address` ON `students`.`Sno` = `student_address`.`students_Sno` ORDER BY `reg_datetime` ASC");

            while (rs.next()) {

                Vector<String> vec = new Vector<>();
                vec.add(rs.getString("Sno"));
                vec.add(rs.getString("stFname") + " " + rs.getString("stLname"));
                vec.add(rs.getString("gender.gen"));
                vec.add(rs.getString("dob"));
                vec.add(rs.getString("stMobile"));
                vec.add(rs.getString("stGuardMobile"));

                model.addRow(vec);
                jTable2.setModel(model);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    
